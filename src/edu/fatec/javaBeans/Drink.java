package edu.fatec.javaBeans;


public class Drink {

	private float refrigerante, run, gelo;

	public float getRefrigerante() {
		return refrigerante;
	}

	public void setRefrigerante(float refrigerante) {
		this.refrigerante = refrigerante;
	}

	public float getRun() {
		return run;
	}

	public void setRun(float run) {
		this.run = run;
	}

	public float getGelo() {
		return gelo;
	}

	public void setGelo(float gelo) {
		this.gelo = gelo;
	}
	
	

}
